                  <li><strong>1928</strong>- Lala Lajpat Rai death and killing of saunders -
                  In 1928, the British government set up the Simon Commission to report on the political situation in India. Some Indian political parties boycotted the Commission because there were no Indians in its membership,[e] and there were protests across the country. When the Commission visited Lahore on 30 October 1928, Lala Lajpat Rai led a march in protest against it.
                  Police attempts to disperse the large crowd resulted in violence.
                  </li>
                  <li><strong>1929</strong>- Assembly Incidence-
                    For some time, Singh had been exploiting the power of drama as a means to inspire the revolt against the British, purchasing a magic lantern to show slides that enlivened his talks about revolutionaries such as Ram Prasad Bismil who had died as a result of the Kakori conspiracy. In 1929, he proposed a dramatic act to the HSRA intended to gain massive publicity for their aims.[22] Influenced by Auguste Vaillant, a French anarchist who had bombed the Chamber of Deputies in Paris,[33] Singh's plan was to explode a bomb inside the Central Legislative Assembly.
                  </li>
                  <li><strong>1930</strong>-Special Tribunal
                    To speed up the slow trial, the Viceroy, Lord Irwin, declared an emergency on 1 May 1930 and introduced an ordinance to set up a special tribunal composed of three high court judges for the case. This decision cut short the normal process of justice as the only appeal after the tribunal was to the Privy Council located in England.
                  </li>
                  <li><strong>1931</strong>- Execution
                    Singh, Rajguru and Sukhdev were sentenced to death in the Lahore conspiracy case and ordered to be hanged on 24 March 1931. The schedule was moved forward by 11 hours and the three were hanged on 23 March 1931 at 7:30 pm[64] in the Lahore jail. It is reported that no magistrate at the time was willing to supervise Singh's hanging as was required by law.
                  </li>
