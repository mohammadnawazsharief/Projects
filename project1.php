                  <li><strong>1907</strong>- Born In Pubjab Province -
                    Bhagat Singh, a Sandhu Jat,[6] was born in 1907[a] to Kishan Singh and Vidyavati at Chak No. 105 GB, Banga village, Jaranwala Tehsil in the Lyallpur district of the Punjab Province of British India(present day Pakistan).
